a = 96
b = 45
c = 56
d = 78

# equal to operator(==)
print('a == b =', a == b)
print('c == d =', c == d)
# not equal to operator(!=)
print('a != b =', a != b)
print('c != d =', c != d)
# greater than operator (>)
print('a > b =', a > b)
print('c > d =', c > d)
# less than operator (<)
print('a < b =', a < b)
print('c < d =', c < d)
# greater than or equal to operator (>=)
print('a >= b =', a >= b)
print('c >= d =', c >= d)
# less than or equal to operator (<=)
print('a <= b =', a <= b)
print('c <= d =', c <= d)